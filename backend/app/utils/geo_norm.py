US_ABBRS = set(US_STATE_TO_ABBR.values())

def normalize_state(s: str) -> Optional[str]:
    if not s:
        return None
    s = s.strip()
    if len(s) == 2 and s.isalpha():
        abbr = s.upper()
        return abbr if abbr in US_ABBRS else None
    return US_STATE_TO_ABBR.get(s.lower())